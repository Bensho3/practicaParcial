public abstract class Vehicle {

    public String name;
    public Float price;
    public String type;

    public Vehicle() {
    }

    public Vehicle(String name, Float price, String type) {
        this.name = name;
        this.price = price;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void showVehicle() {

    }


}
